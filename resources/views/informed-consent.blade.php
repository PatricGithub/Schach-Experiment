                    <h1>Einverständniserklärung</h1>
                    <p class="magazin_paragraph">Bitte lese dir die Informationen sorgfältig durch. Du musst mindestens 18 Jahre alt sein, um an dieser Studie teilnehmen zu können.</p>
                    
                    <h3 style="margin-bottom: 0 !important; padding-bottom: 0!important"><strong>Zweck der Forschung</strong></h3>
                    <p class="magazin_paragraph" style="margin-top: 10px !important; padding-top: 0px!important">
                        Das Ziel der Studie ist es, Analogien im Schach zu untersuchen. 
                        Sie wird im Rahmen wissenschaftlicher Forschung durchgeführt, die vom Fachbereich Kognitionswissenschaft und Psychologie NBU durchgeführt wird. 
                        Die Studie wurde von der Ethikkommission der NBU genehmigt.</p>
                    
                    <h3><strong>Ablauf</strong></h3>
                    <p class="magazin_paragraph" style="margin-top: 10px !important; padding-top: 0px!important">
                        Zuerst wirst du einen kurzen Fragebogen beantworten und dann drei Schachpuzzles lösen. Diese Aufgaben dienen dazu, dein Schachniveau einzuschätzen und festzustellen, ob du ein Anfänger oder ein fortgeschrittener Spieler bist.
                             <br>
<br>
                            <strong>Das wird von dir erwartet:</strong> <br>
                            Du wirst mit 10 Schachpartien nacheinander konfrontiert. Neben jeder Schachpartie wird dir ein leeres Schachbrett bereitgestellt, welches du mit Schachfiguren mittel Drag and Drop Funktion bestücken kannst. 
                            Deine Aufgabe ist es nun eine Analogie zu generieren. Versuche eine Partie mit einer Analogie aus deiner Erfahrung zu generieren oder eine neue Analogie zu erfinden, in der eine ähnliche Situation auf dem Brett entsteht. 
                            Anschließend sollst du erklären wieso du deine Analogie gewählt hast. 
                            Zusätzlich sollst du noch die Faszination, die du für deine Brettzusammenstellung empfindest, bestimmen.
                             <br>
                            <br> <strong>Vorab wichtig zu wissen:</strong>
                            <br> 
                            Stell dir vor, du kennst schon etwas und kannst damit herausfinden, wie etwas anderes funktioniert, das du noch nicht kennst. Das ist genau das, was Denken mit Analogien ermöglicht.
                            <br>
                            <strong>Beispiel:</strong>
                            <br>
                            <strong>Bekannt:</strong> Hänsel und Gretel essen das Lebkuchenhaus, ohne zu merken, dass sie ihr Zuhause zerstören.
                            <br><strong> Unbekannt:</strong> Manche Länder nutzen die Ressourcen der Natur auf, ohne zu bedenken, dass sie damit das Klima zerstören.
                            Weder Hänsel und Gretel noch ihr Lebkuchenhaus haben etwas mit dem Klimawandel zu tun.  Aber die Art und Weise, wie sie ihr Zuhause zerstören, ähnelt der Art und Weise, wie wir unseren Planeten zerstören, indem wir Ressourcen ohne Rücksicht auf die Folgen verbrauchen.  Genau diese Art der Ähnlichkeit solltest du beim Befüllen des leeren Bretts berücksichtigen.
                                                </p>
                    
                    <h3><strong>Dauer</strong></h3>
